#include <iostream>
using namespace std;

int main()
{
	int input;
	cin >> input;

	int numOfRow = 1;
	while (input > numOfRow)
	{
		input -= numOfRow;
		numOfRow++;
	}
	
	if (numOfRow % 2 == 1)
	{
		cout << numOfRow + 1 - input << '/' << input << endl;
	}
	else
	{
		cout << input << '/' << numOfRow + 1 - input << endl;
	}
}